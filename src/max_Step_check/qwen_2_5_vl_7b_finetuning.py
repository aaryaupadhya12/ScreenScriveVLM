from huggingface_hub import login
from unsloth import FastVisionModel
import torch

login(token="Use your HF token here ")  # paste your token here


model, tokenizer = FastVisionModel.from_pretrained(
    model_name = "unsloth/Qwen2.5-VL-7B-Instruct-bnb-4bit",
    max_seq_length = 2048,
    load_in_4bit = True,
    dtype = None,
    use_gradient_checkpointing = "unsloth",
)

model = FastVisionModel.get_peft_model(
    model,
    finetune_vision_layers     = True,
    finetune_language_layers   = True,
    finetune_attention_modules = True,
    finetune_mlp_modules       = True,
    r = 16,
    lora_alpha = 16,
    lora_dropout = 0,
    bias = "none",
    random_state = 3407,
)

from datasets import load_dataset

raw_dataset = load_dataset("rootsautomation/RICO-Screen2Words", split="train")
raw_dataset = raw_dataset.shuffle(seed=42).select(range(700))

from tqdm import tqdm

instruction = "You are a UI/UX expert. Describe what you see on this mobile app screen."

def convert_to_conversation(sample):
    return {
        "messages": [
            {
                "role": "user",
                "content": [
                    {"type": "text",  "text": instruction},
                    {"type": "image", "image": sample["image"]},
                ]
            },
            {
                "role": "assistant",
                "content": [{"type": "text", "text": sample["captions"][0]}]
            },
        ]
    }

converted_dataset = []
for i in tqdm(range(len(raw_dataset)), desc="Converting dataset"):
    try:
        converted_dataset.append(convert_to_conversation(raw_dataset[i]))
    except Exception as e:
        print(f"Skipping sample {i} due to error: {e}")
        continue

print(f"Successfully converted {len(converted_dataset)} samples")
print("Sample 0 keys:", converted_dataset[0]["messages"][0]["content"])

converted_dataset[0]

"""# Training / Fine-tuning Block"""

from unsloth.trainer import UnslothVisionDataCollator
from trl import SFTTrainer, SFTConfig

FastVisionModel.for_training(model)


trainer = SFTTrainer(
    model = model,
    tokenizer = tokenizer,
    data_collator = UnslothVisionDataCollator(model, tokenizer),
    train_dataset = converted_dataset,
    args = SFTConfig(
        per_device_train_batch_size = 2, # How many samples are processed per GPU per step
        gradient_accumulation_steps = 4, # Simulates a larger batch without using more VRAM
        warmup_steps = 5, # learning rates starts near 0 and ramps up over 5 steps
        max_steps = 30, #
        learning_rate = 2e-4, # Standard for lora Fine tuning , Too high = Unstable updating , low = no learning
        fp16 = False,
        bf16 = False,
        logging_steps = 1,
        optim = "adamw_8bit",
        weight_decay = 0.01,
        lr_scheduler_type = "cosine", # they use cosine curve down to near 0 by the end , smoother than decay
        seed = 3407,
        output_dir = "outputs",
        report_to = "none",
        remove_unused_columns = False,
        dataset_text_field = "", # tells SFFTrainer tht there is no text column to tokenize
        dataset_kwargs = {"skip_prepare_dataset": True},
        dataset_num_proc = 4,
        max_seq_length = 2048,
    ),
)

# Batch caluculation
# Per decvice train batch size = 2
# gradient accumalation step = 4
# --------------------------------
# Total = 2 x 4  = 8 : Effective 8
# 700 samples / 8 = 88 steps for 1 full epoch
# In 30 steps we would see 30 x 8 samples = 240 samples
# 240 / 700 = 24%

gpu_stats = torch.cuda.get_device_properties(0)
start_gpu_memory = round(torch.cuda.max_memory_reserved() / 1024 / 1024 / 1024, 3)
max_memory = round(gpu_stats.total_memory / 1024 / 1024 / 1024, 3)
print(f"GPU = {gpu_stats.name}. Max memory = {max_memory} GB.")
print(f"{start_gpu_memory} GB of memory reserved.")

trainer_stats = trainer.train()

# @title Show final memory and time stats
used_memory = round(torch.cuda.max_memory_reserved() / 1024 / 1024 / 1024, 3)
used_memory_for_lora = round(used_memory - start_gpu_memory, 3)
used_percentage = round(used_memory / max_memory * 100, 3)
lora_percentage = round(used_memory_for_lora / max_memory * 100, 3)
print(f"{trainer_stats.metrics['train_runtime']} seconds used for training.")
print(
    f"{round(trainer_stats.metrics['train_runtime']/60, 2)} minutes used for training."
)
print(f"Peak reserved memory = {used_memory} GB.")
print(f"Peak reserved memory for training = {used_memory_for_lora} GB.")
print(f"Peak reserved memory % of max memory = {used_percentage} %.")
print(f"Peak reserved memory for training % of max memory = {lora_percentage} %.")



